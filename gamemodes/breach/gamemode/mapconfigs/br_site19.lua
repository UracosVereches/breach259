function SpawnLoot(function_to_use, positions_table)
	for k, v in ipairs(positions_table) do
		ent_class = nil
		local ent_class = function_to_use

		if ent_class != nil then
			local ent = ents.Create(ent_class)
			ent:Spawn()
			ent:SetPos(v)
			round_total_entities = round_total_entities + 1
		end
	end
end

